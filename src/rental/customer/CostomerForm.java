    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
                                          
        int selectedRow = tblCustomers.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a customer from the table to delete!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this customer?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
     
        if (confirm == JOptionPane.YES_OPTION) {
            String idToDelete = tableModel.getValueAt(selectedRow, 0).toString();
            for (int i = 0; i < CustomerHelpers.customers.size(); i++) {
                if (CustomerHelpers.customers.get(i).getCustomerId().equals(idToDelete)) {
                    CustomerHelpers.customers.remove(i);
                    break; 
                }
            }
                
            CustomerHelpers.writeFile();
            refreshTable();
            
            JOptionPane.showMessageDialog(this, "Customer deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } 
    
    }//GEN-LAST:event_btnDeleteActionPerformed

