    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       
        String id = txtId.getText();
        String name = txtName.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String license = txtLicense.getText();
        String expiry = txtExpiry.getText();
        String natId = txtNatId.getText();
        String status = "ACTIVE"; 
        
        if (id.isEmpty() || name.isEmpty() || phone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in the ID, Name, and Phone fields!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!isValidEmail(email)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
         if (CustomerHelpers.exists(id)) {
            JOptionPane.showMessageDialog(this, "A customer with this ID already exists!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
         // creat new customer
         Customer newCustomer = new Customer(id, name, phone, email, address, license, expiry, natId, status);
         CustomerHelpers.customers.add(newCustomer);//Adding
         CustomerHelpers.writeFile();//Saving
          
        refreshTable();
        clearFields();
        

 JOptionPane.showMessageDialog(this, "Customer added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
 
 
    }//GEN-LAST:event_btnAddActionPerformed

